# Python logical operators allow us to perform logical AND, OR, and NOT operation between boolean values.
# they always return a boolean value in return

x = True
y = False

# and -	Logical AND: True if both the operands are true	
print(x and y)

# or - Logical OR: True if either of the operands is true	
print(x or y)

# not - Logical NOT: True if operand is false	
print(not x)