# Arithmetic Operators
# Arithmetic operators are used to perform arithmetic operations between two operands. It includes 
# + (addition), 
print(3+3)
# - (subtraction), 
print(12-45)
# *(multiplication), 
print(3*4)
# /(divide), 
print(20/2)
# %(reminder), 
print(10%3)
# //(floor division),
print(10//3) 
# and exponent (**) operators.
print(2**8)